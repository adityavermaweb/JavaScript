//Sum of two number with Arrow Function
const sum = (a, b) =>{
    console.log(a+b);
};

sum(2, 3);


//Cube of number
const cube = (n) => {
    console.log (n * n * n); //Use return also
};

cube(2);


//Power of number
const pow = (a, b) => {
    console.log(a ** b); //Use return also
}

pow(2, 2);


//No argument in arrow function
const hello = () => {
    console.log("hello world");
}

hello();